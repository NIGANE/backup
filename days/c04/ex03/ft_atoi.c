int	ft_atoi(char	*str)
{
	int	i;
	int	sign;
	int	re;

	i = 0;
	sign = 1;
	while (str[i] != '\0')
	{
		if(str[i] == '-')
			sign *= -1;
		else if (str[i] == ' ')
			sign = 1;
		else if (str[i] >= '0' && str[i] <= '9')
		{
			while (str[i] >= '0' && str[i] <= '9')
			{
				re = (re * 10) + (str[i++] - '0');
			}
			return re * sign;
		}
		i++;
	}
	return 0;
}
