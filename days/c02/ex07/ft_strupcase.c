int is_lower(char a)
{
    if (a >= 'a' && a <= 'z')
    {
        return (1);
    }
    return (0);
}

char    *ft_strupcase(char* str)
{
    int i;

    i = 0;
    while (str[i] != '\0')
    {
        if (is_lower(str[i]))
        {
            str[i] = str[i] - ('a' - 'A');
        }
        i++;
    }
    return (str);
}