int	ft_iterative_factorial(int nb)
{
	int	re;

	re = 1;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (nb-- > 0)
		re = re * nb;
	return (re);
}
